#include <iostream>
using namespace std;
int main(){
    // Increment Operator
    int a = 10;
    cout << ++a << endl;
    a++;
    cout << a  << endl;
    cout << a++ << endl;
    cout << a << endl;
    // Decrement Operator
    int b = 10;
    cout << --b << endl;
    --b;
    cout << b << endl;
    cout << b-- << endl;
    cout << b << endl;
    return 0;
}