#include <iostream>
using namespace std;
int main()
{
    int a = 10;
    a += 1; // Shortcut for a = a+1
    cout << a << endl;
    a -= 1; // Shortcut for a = a-1
    cout << a << endl;
    a *= 3; // Shortcut for a = a*3
    cout << a << endl;
    a/= 5; // Shortcut for a = a/5
    cout << a << endl;
    return 0;
}