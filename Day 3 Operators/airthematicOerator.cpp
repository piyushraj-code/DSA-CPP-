#include <iostream>
using namespace std;
int main(){
    int a = 10;
    int b = 4;
    // Addition operator
    int sum = a+b;
    cout << sum << endl;
    // Substraction operator
    int sub = a-b;
    cout << sub << endl;
    // Multiplication operator
    int mul = a*b;
    cout << mul << endl;
    // Division operator
    float div = (float)a/b;
    cout << div << endl;
    // Modulo operator
    int remainder = a%b;
    cout << remainder << endl;
    return 0;
}